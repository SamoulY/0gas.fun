- 倾向于提供全面、多角度的解释，即使问题很简单。
- 字数通常较多（超过30字），且内容完整。
- 可能包含“因为”、“所以”、“例如”、“比如”等解释性词语。
- 如果回答中有多个句子，且句子之间逻辑连贯，通常是AI。

